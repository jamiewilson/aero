/** JavaScript reserved words and literals that must not be treated as variable references. */
export const JS_RESERVED_IDENTIFIERS: ReadonlySet<string> = new Set([
	'await',
	'as',
	'break',
	'case',
	'catch',
	'class',
	'const',
	'continue',
	'debugger',
	'default',
	'delete',
	'do',
	'else',
	'enum',
	'export',
	'extends',
	'false',
	'finally',
	'for',
	'from',
	'function',
	'if',
	'import',
	'in',
	'instanceof',
	'let',
	'new',
	'null',
	'of',
	'return',
	'super',
	'switch',
	'this',
	'throw',
	'true',
	'try',
	'typeof',
	'undefined',
	'var',
	'void',
	'while',
	'with',
	'yield',
])

export function isJsReservedIdentifier(name: string): boolean {
	return JS_RESERVED_IDENTIFIERS.has(name)
}
