/**
 * Reads `src/render/templates/*.css` and `*.html` and writes
 * `generated-assets.ts`. Consumers resolve `@aero-js/diagnostics` from `dist/`, so
 * after editing templates run `pnpm embed-templates` (regenerates + `tsdown`) or
 * `pnpm build`; `pnpm gen-templates` alone only updates `src/` (enough for vitest/tsc).
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = join(__dirname, '..')
const cssPath = join(root, 'src/render/templates/overlay.css')
const htmlPath = join(root, 'src/render/templates/error-page.html')
const outPath = join(root, 'src/render/templates/generated-assets.ts')

const OVERLAY_CSS = readFileSync(cssPath, 'utf8')
const ERROR_PAGE_HTML_TEMPLATE = readFileSync(htmlPath, 'utf8')

const out =
	`// Auto-generated by scripts/embed-templates.mjs — do not edit by hand.\n` +
	`export const OVERLAY_CSS: string = ${JSON.stringify(OVERLAY_CSS)}\n` +
	`export const ERROR_PAGE_HTML_TEMPLATE: string = ${JSON.stringify(ERROR_PAGE_HTML_TEMPLATE)}\n`

writeFileSync(outPath, out, 'utf8')
